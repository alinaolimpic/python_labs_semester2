from model import Bus
import time

def print_separator(scenario_name):
    """Функция для вывода разделителей между сценариями"""
    print("\n" + "=" * 70)
    print(f" {scenario_name}")
    print("=" * 70)

def demonstrate_all_scenarios():
    
    # ==================== СЦЕНАРИЙ 1: СОЗДАНИЕ И ВЫВОД АВТОБУСА ====================
    print_separator("СЦЕНАРИЙ 1: СОЗДАНИЕ И ВЫВОД АВТОБУСА")
    
    print("\n▶ Создание объекта Bus с параметрами (маршрут '101', вместимость 30, скорость 60, водитель 'Иванов Иван')")
    bus1 = Bus("101", 30, 60.0, "Иванов Иван")
    
    print("\n▶ Вывод через print(bus1) вызывает магический метод __str__:")
    print(bus1)
    
    print("\n▶ Вывод через repr(bus1) для разработчика:")
    print(repr(bus1))
    
    print("\n✅ Проверка: конструктор создал объект с уникальным ID, __str__ выводит полную информацию")
    
    # ==================== СЦЕНАРИЙ 2: СРАВНЕНИЕ АВТОБУСОВ ====================
    print_separator("СЦЕНАРИЙ 2: СРАВНЕНИЕ АВТОБУСОВ")
    
    print("\n▶ Создаем два автобуса с одинаковыми маршрутом и водителем:")
    bus_same = Bus("101", 30, 60.0, "Иванов Иван")
    
    print("\n▶ Создаем автобус с другим водителем:")
    bus_diff = Bus("101", 30, 60.0, "Петров Петр")
    
    print("\n▶ Выполняем операцию сравнения bus1 == bus_same:")
    print(f"   Результат: {bus1 == bus_same}")
    print("   (Метод __eq__ сравнивает маршрут и водителя → True)")
    
    print("\n▶ Выполняем операцию сравнения bus1 == bus_diff:")
    print(f"   Результат: {bus1 == bus_diff}")
    print("   (Метод __eq__ сравнивает маршрут и водителя → False)")
    
    print("\n✅ Демонстрация работы метода __eq__: автобусы считаются равными, если совпадают маршрут и водитель")
    
    # ==================== СЦЕНАРИЙ 3: SETTER И ВАЛИДАЦИЯ СКОРОСТИ ====================
    print_separator("СЦЕНАРИЙ 3: SETTER И ВАЛИДАЦИЯ СКОРОСТИ")
    
    bus3 = Bus("202", 40, 60.0, "Сидоров Сидор")
    print(f"\n▶ Создается автобус с начальной скоростью {bus3.speed} км/ч")
    
    print("\n▶ Через свойство speed изменяем скорость на 80 км/ч:")
    bus3.speed = 80
    print(f"   Новая скорость: {bus3.speed} км/ч — успешно")
    
    print("\n▶ Пытаемся изменить скорость на 150 км/ч (превышает лимит 120 км/ч):")
    try:
        bus3.speed = 150
    except ValueError as e:
        print(f"   ОШИБКА: {e}")
    print(f"   Скорость осталась: {bus3.speed} км/ч")
    
    print("\n✅ Проверка работы сеттера с валидацией: нельзя установить скорость больше 120 км/ч")
    
    # ==================== СЦЕНАРИЙ 4: АТРИБУТЫ КЛАССА ====================
    print_separator("СЦЕНАРИЙ 4: АТРИБУТЫ КЛАССА")
    
    print("\n▶ Создаем три автобуса:")
    b1 = Bus("10", 20, 50, "Водитель A")
    b2 = Bus("20", 40, 55, "Водитель B")
    b3 = Bus("30", 30, 60, "Водитель C")
    
    print(f"\n▶ Атрибут класса Bus.total_buses = {Bus.total_buses}")
    print(f"▶ Обращение к атрибуту через экземпляр b1.total_buses = {b1.total_buses}")
    print(f"   (поиск идет в классе → одинаковое значение)")
    
    print("\n✅ Демонстрация разницы между атрибутами класса и экземпляра")
    
    # ==================== СЦЕНАРИЙ 5: СОСТОЯНИЕ ОБЪЕКТА ====================
    print_separator("СЦЕНАРИЙ 5: СОСТОЯНИЕ ОБЪЕКТА")
    
    bus5 = Bus("404", 35, 55, "Иванов Иван")
    print(f"\n▶ Создается автобус со статусом: {bus5.status}")
    
    print("\n▶ Посадка 10 пассажиров (в парке — разрешено):")
    result = bus5.board_passengers(10)
    print(f"   Село: {result['boarded']} пассажиров, всего в автобусе: {bus5.passenger_count}")
    
    print("\n▶ Выезд на маршрут:")
    bus5.start_route()
    print(f"   Статус после выезда: {bus5.status}")
    
    print("\n▶ Автобус попадает в аварию через метод accident():")
    bus5.accident()
    print(f"   Статус после аварии: {bus5.status}")
    print(f"   Пассажиров в автобусе после аварии: {bus5.passenger_count} (часть пострадала)")
    
    print("\n▶ Пытаемся выполнить посадку 5 пассажиров после аварии (запрещено):")
    try:
        bus5.board_passengers(5)
    except ValueError as e:
        print(f"   ОШИБКА: {e}")
    
    print("\n▶ Пытаемся выехать на маршрут после аварии (запрещено):")
    try:
        bus5.start_route()
    except ValueError as e:
        print(f"   ОШИБКА: {e}")
    
    print("\n✅ Состояние автобуса блокирует все операции, кроме ремонта")
    
    # ==================== СЦЕНАРИЙ 6: ОШИБКИ СОЗДАНИЯ ====================
    print_separator("СЦЕНАРИЙ 6: ОШИБКИ СОЗДАНИЯ")
    
    test_cases = [
        ("101", -10, 60, "Водитель", "вместимость = -10", "val_capacity()"),
        ("101", 50, 0, "Водитель", "скорость = 0", "val_speed()"),
        ("101", 50, 60, "", "имя водителя = '' (пустая строка)", "val_driver_name()"),
        ("", 50, 60, "Водитель", "номер маршрута = '' (пустая строка)", "val_route_number()"),
        ("101", 50.5, 60, "Водитель", "вместимость = 50.5 (не целое число)", "val_capacity()"),
    ]
    
    for route, cap, speed, driver, desc, func in test_cases:
        print(f"\n▶ Попытка: {desc}")
        print(f"   Проверяет функция: {func}")
        try:
            bad = Bus(route, cap, speed, driver)
        except (TypeError, ValueError) as e:
            print(f"   ОШИБКА: {e}")
    
    print("\n✅ Все исключения перехватываются, объекты не создаются")
    
    # ==================== СЦЕНАРИЙ 7: ПОЛНЫЙ ЦИКЛ РАБОТЫ АВТОБУСА ====================
    print_separator("СЦЕНАРИЙ 7: ПОЛНЫЙ ЦИКЛ РАБОТЫ АВТОБУСА (БИЗНЕС-ЛОГИКА)")
    
    bus7 = Bus("777", 30, 65, "Петров Петр")
    print(f"\n▶ Создается автобус с полным баком: {bus7.fuel_level}% топлива")
    print(f"   Начальное состояние: {bus7}")
    
    print("\n▶ 1. Посадка 25 пассажиров (из 30 мест):")
    bus7.board_passengers(25)
    print(f"   Пассажиров в автобусе: {bus7.passenger_count}")
    
    print("\n▶ 2. Выезд на маршрут через start_route():")
    bus7.start_route()
    print(f"   Статус после выезда: {bus7.status}")
    
    print("\n▶ 3. Завершение маршрута через finish_route():")
    bus7.finish_route()
    print(f"\n   Результат:")
    print(f"   • Статус: {bus7.status}")
    print(f"   • Пассажиры высажены: {bus7.passenger_count}")
    print(f"   • Пробег: {bus7.mileage} км (+50 км)")
    print(f"   • Остаток топлива: {bus7.fuel_level}% (-25%)")
    
    print("\n✅ Проверка бизнес-логики: посадка → маршрут → завершение с обновлением параметров")
    
    # ==================== СЦЕНАРИЙ 8: РАБОТА С НЕСКОЛЬКИМИ АВТОБУСАМИ ====================
    print_separator("СЦЕНАРИЙ 8: РАБОТА С НЕСКОЛЬКИМИ АВТОБУСАМИ")
    
    print("\n▶ Создаем три автобуса с разной вместимостью:")
    bus_a = Bus("A", 20, 50, "Водитель A")
    bus_b = Bus("B", 40, 55, "Водитель B")
    bus_c = Bus("C", 30, 60, "Водитель C")
    
    print("\n▶ Явная демонстрация __lt__ (сравнение по вместимости):")
    print(f"   bus_a (вместимость 20) < bus_c (вместимость 30): {bus_a < bus_c}")
    print(f"   bus_c (вместимость 30) < bus_b (вместимость 40): {bus_c < bus_b}")
    print(f"   bus_b (вместимость 40) < bus_a (вместимость 20): {bus_b < bus_a}")
    
    buses = [bus_b, bus_a, bus_c]
    
    print("\n▶ До сортировки:")
    for b in buses:
        print(f"   Маршрут {b.route_number}: вместимость {b.capacity}")
    
    print("\n▶ Вызываем функцию sorted() для сортировки списка (использует __lt__):")
    buses.sort()
    
    print("\n▶ После сортировки (по вместимости, благодаря __lt__):")
    for b in buses:
        print(f"   Маршрут {b.route_number}: вместимость {b.capacity}")
    
    print("\n✅ Демонстрация магического метода __lt__: сравнение и сортировка по вместимости")
    
    # ==================== ИТОГОВАЯ СТАТИСТИКА ====================
    print_separator("ИТОГОВАЯ СТАТИСТИКА")
    
    print(f"\n▶ Всего создано автобусов: {Bus.total_buses}")
    print(f"▶ Всего перевезено пассажиров: {Bus.total_passengers_transported}")
    
    print("\n▶ Финальное состояние всех автобусов:")
    all_buses = [bus1, bus_same, bus_diff, bus3, b1, b2, b3, bus5, bus7, bus_a, bus_b, bus_c]
    for bus in all_buses:
        print(f"   {bus}")

if __name__ == "__main__":
    demonstrate_all_scenarios()